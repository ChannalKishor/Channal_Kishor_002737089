    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblRecords.getModel();
        model.setRowCount(0);
        
        //clean table
        for (Employee ep : records.getRecords()){
            
            Object row[] = new Object[4];
            row[0] = ep;
            row[1] = ep.getEid();
            row[2] = ep.getLevel();
            row[3] = ep.getPositionTable();
            
            model.addRow(row);
        }
    }
